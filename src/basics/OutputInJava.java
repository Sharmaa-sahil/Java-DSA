public class OutputInJava{
    public static void main(String args[]){
        // Basic print
        System.out.println("Hello Sahil");

        // Print multiple lines 
        System.out.println("this is the second line");
        System.out.println("this is the third line");


        // Print on the same line 
        System.out.print("this ");
        System.out.print("is ");
        System.out.print("on the same line ");

        // Line break
        System.out.println(); //It creates a new line


        // Numbers and text 
        System.out.println("The number is: " + 42);
    }
}